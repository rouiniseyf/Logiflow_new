function getColor(entry:any) {

  const number = parseInt(entry, 10);

  switch (number) {
      case 1:
          return 'green';
      case 2:
          return 'orange';
      case 3:
          return 'yellow';
      case 4:
          return 'red';
      case 5:
          return 'blue';
      case 6:
          return 'indigo';
      case 7:
          return 'violet';
      case 8:
          return 'brown';
      case 9:
          return 'pink';
      case 10:
          return 'gray';
      default:
        return 'indigo';
  }
}


